function generateDailyTrend() {
  const data = [];
  for (let i = 1; i <= 30; i++) {
    data.push({
      day: `${i}日`,
      revenue: 3000 + Math.round(Math.sin(i / 3) * 1500) + Math.floor(Math.random() * 1000),
      visitors: 60 + Math.round(Math.sin(i / 4) * 30) + Math.floor(Math.random() * 20),
    });
  }
  return data;
}

